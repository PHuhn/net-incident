        /// <summary>
        /// POST: /Account/Register
        /// </summary>
        /// <param name="model">RegisterBindingModel</param>
        /// <returns>view model</returns>
        [HttpPost]
        [AllowAnonymous]
        [ValidateAntiForgeryToken]
        public async Task<ActionResult> Register(RegisterBindingModel model)
        {
            if (ModelState.IsValid)
            {
                //
                // Get server to be added (and also defines default company)...
                //
                ApplicationServer _server = await ServerManager.FindByNameAsync(model.ServerShortName);
                if (_server != null)
                {
                    int _companyId = _server.CompanyId;
                    ApplicationUser _user = new ApplicationUser()
                    {
                        UserName = model.UserName,
                        CompanyId = _companyId,
                        Email = model.Email,
                        FirstName = model.FirstName,
                        LastName = model.LastName,
                        FullName = String.Format("{0} {1}", model.FirstName, model.LastName),
                        UserNicName = (model.FirstName.Length > 16 ? model.FirstName.Substring(0, 16) : model.FirstName),
                        CreateDate = DateTime.Now
                    };
                    //
                    // Add server that was passed as a part of the binding model...
                    //
                    _user.Servers = new List<ApplicationServer>();
                    _user.Servers.Add(_server);
                    //
                    // Add default user role...
                    //
                    ApplicationUserRole _urole = new ApplicationUserRole()
                    {
                        UserId = _user.Id,
                        RoleId = "pub"
                    };
                    _user.Roles.Add(_urole);
                    // add user with defined server and default role
                    var result = await UserManager.CreateAsync(_user, model.Password);
                    if (result.Succeeded)
                    {
                        var code = await UserManager.GenerateEmailConfirmationTokenAsync(_user.Id);
                        var callbackUrl = Url.Action("ConfirmEmail", "Account", new { userId = _user.Id, code = code }, protocol: Request.Url.Scheme);
                        await UserManager.SendEmailAsync(_user.Id, "Confirm your account", "Please confirm your account by clicking this link: <a href=\"" + callbackUrl + "\">link</a>");
                        ViewBag.Link = callbackUrl;
                        return View("DisplayEmail");
                    }
                    AddErrors(result);
                }
                else
                    ModelState.AddModelError("", string.Format(NSG.Identity.Constants.ServerNotFoundException, _codeName, model.ServerShortName));

            }
            // If we got this far, something failed, redisplay form
            Base_AddErrors(ModelState);
            return View(model);
        }

